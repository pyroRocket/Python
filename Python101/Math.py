# Math

print("Math time: ")

print(67 + 69)  # Add

print(67 - 69)  # Subtract

print(67 * 69)  # Multiply

print(10 / 5)  # Divide

print(20 + 20 - 20 * 20 / 20)  # PEMDAS (Order of operations)

print(20 ** 2)  # Exponents (20 ^ 2)

print(23 % 4)  # Modulo will return the remainder of a division operation

print(10 // 5)  # truncated division, if there is a decimal it will be dropped
