# Variables

# Without variables:
print("There once was a man named George")
print("He was 70 years old")
print("He really liked the name George")
print("But he didnt like being 70")
print("\n")  # new line

characterName = "John"
characterAge = 35

print("There once was a man named " + characterName )
print("He was " + str(characterAge) + " years old")
print("He really liked the name " + characterName)
print("But he didnt like being " + str(characterAge))

characterName = "Sean"
print("There once was a man named " + characterName )

